package battleship;

/** * Simple class which holds two integer values.
 * @author Kellin McAvoy, Nathan Kelderman, Sean Thomas */
public class Point {
	/** Integer x value. */
	public int x;
	
	/** Integer y value. */
	public int y;
	
	/** Constructor which sets both integer values.
	 * @param x First integer value.
	 * @param y Second integer value. */
	public Point(final int x, final int y) {
		this.x = x;
		this.y = y;
	}
}
